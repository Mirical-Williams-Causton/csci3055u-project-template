/**
  Variables
  - Declaring Variables 
    - In swift, variables are declared using the 'var' keyword.
    - Unlike most strongly-typed languages, you do not need a ';' after declaring your variables.
    - Variables cannot be used before being initialized.
    - Variable types must be implicited or explicited specified, meaning, variables either have to
      be assigned a value to specify its type or explicitly specify its type upon declaration.
*/
